# Operasi Aritmatika dalam Python

a = 10
b = 3

# Operasi Pertambahan (+)
hasil = a+b
print(a,'+',b,'=',hasil)

# Operasi Pengurangan (-)
hasil = a-b
print(a,'-',b,'=',hasil)

# Operasi Peekalian (*)
hasil = a*b
print(a,'*',b,'=',hasil)

# Operasi Pemabagian (/)
hasil = a/b
print(a,'/',b,'=',hasil)

# Operasi Eksponen / Pangkat (**)
hasil = a**b
print(a,'**',b,'=',hasil)

# Operasi Modulus / Hasil dari sisa pembagian (%)
hasil = a%b
print(a,'%',b,'=',hasil)

# Operasi Floor Division / Pembulatan hasil dari pembagian (//)
hasil = a//b
print(a,'//',b,'=',hasil)

# Prioritas Operasi Aritmatika
hasil = a+b*a/b
print(a,'+',b,'*',a,'/',b,'=',hasil)