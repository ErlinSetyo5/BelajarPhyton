# 1. Cara membuat string

'''
Ada dua cara untuk membuat string
    1. Dengan menggunakan Single Quote ('...')
    2. Dengan menggunakan Double Quote ('...')
'''

# Contoh Penggunaan Single Quote '...'
data = 'Ayo kita nonton film korea'
print(data)

print("\n----- INI ADALAH PEMBATAS -----\n")

# Contoh Penggunaan Double Quote "..."
data = "Ayo kita nonton film korea"
print(data)

print("\n----- INI ADALAH PEMBATAS -----\n")

